function carregar() {
    var msg = window.document.getElementById('msg')
    var img = window.document.getElementById('imagem')
    var cump = window.document.getElementById('cump')
    var data = new Date()
    var hora = data.getHours()
    msg.innerHTML = 'agora sÃ£o ' + hora + ' horas.'
    if (hora >= 0 && hora < 12) {
        cump.innerHTML = 'BOM DIA'
        img.src = 'fotomanha.png'
        document.body.style.background = '#fec5b2'
    } else if (hora >= 12 & hora < 18) {
        cump.innerHTML = 'BOA TARDE'
        img.src = 'fototarde.png'
        document.body.style.background = '#94b998'
    } else {
        cump.innerHTML = 'BOA NOITE'
        img.src = 'fotonoite.png'
        document.body.style.background = '#171c2a'
    }
}